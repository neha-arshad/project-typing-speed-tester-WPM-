export class User {
    name;
    surName;
    age;
    gender;
    EmailID;
    password;
    phone;
    constructor(name, surName, age, gender, EmailID, password, phone) {
        this.name = name;
        this.surName = surName;
        this.age = age;
        this.gender = gender;
        this.EmailID = EmailID;
        this.password = password;
        this.phone = phone;
    }
}
